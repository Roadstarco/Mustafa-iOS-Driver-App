import Foundation
import UIKit

/// UINavigationController extension
/// Extension for adding **Identifiers** to UINavigationController.
extension UINavigationController {
    /**
     
     All UINavigationControllers which are used across the app have their **Identifiers** defined here
     
     */
    enum Identifiers {
        static let WelcomNav    = "WelcomNav"
        static let HomeNav      = "HomeNav"
    }
}
